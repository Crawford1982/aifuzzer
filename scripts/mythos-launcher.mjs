#!/usr/bin/env node
/**
 * Minimal local GUI: paste a target URL and start Mythos from the browser.
 * Binds 127.0.0.1 only. Defaults match common bounty rate rules (5 rps, 5 concurrent).
 */
import http from 'node:http';
import { spawn } from 'node:child_process';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.join(__dirname, '..');
const mainJs = path.join(repoRoot, 'src', 'main.js');

/** Defaults — conservative for in-scope automation (many programs cap ~5 rps / low concurrency). */
const PRESET_BOUNTY = {
  maxRps: 5,
  concurrency: 5,
  maxRequests: 200,
  timeoutMs: 8000,
};

const PORT = Number(process.env.MYTHOS_LAUNCHER_PORT) || 18791;

function openBrowser(url) {
  if (process.platform === 'win32') {
    spawn('cmd', ['/c', 'start', '', url], { detached: true, stdio: 'ignore' }).unref();
  } else if (process.platform === 'darwin') {
    spawn('open', [url], { detached: true, stdio: 'ignore' }).unref();
  } else {
    spawn('xdg-open', [url], { detached: true, stdio: 'ignore' }).unref();
  }
}

/**
 * @param {string} targetUrl
 * @returns {string} path to written scope yaml
 */
function writeAutoScope(targetUrl) {
  let hostname = 'localhost';
  let scheme = 'https';
  try {
    const u = new URL(targetUrl.trim());
    hostname = u.hostname;
    scheme = u.protocol === 'https:' ? 'https' : 'http';
  } catch {
    throw new Error('Invalid target URL');
  }

  const dir = path.join(repoRoot, 'output', '.launcher');
  fs.mkdirSync(dir, { recursive: true });
  const filePath = path.join(dir, `scope-auto-${Date.now()}.yaml`);
  const yaml = [
    `# Auto-generated by mythos-launcher — host from your target URL`,
    `allowHosts:`,
    `  - ${hostname}`,
    `pathPrefixes:`,
    `  - /`,
    `schemes:`,
    `  - ${scheme}`,
    '',
  ].join('\n');
  fs.writeFileSync(filePath, yaml, 'utf8');
  return filePath;
}

function escapeHtml(s) {
  return String(s)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}

