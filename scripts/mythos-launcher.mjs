/**
 * @param {import('http').IncomingMessage} req
 * @returns {Promise<Record<string, string>>}
 */
async function parseForm(req) {
  const chunks = [];
  for await (const c of req) chunks.push(c);
  const raw = Buffer.concat(chunks).toString('utf8');
  const params = new URLSearchParams(raw);
  /** @type {Record<string, string>} */
  const out = {};
  for (const [k, v] of params.entries()) out[k] = v;
  return out;
}

/**
 * @param {Record<string, string>} form
 */
function buildArgs(form) {
  const target = (form.target || '').trim();
  if (!target) throw new Error('Target is required');

  const args = ['--target', target];

  const maxRps = Number(form.maxRps);
  const concurrency = Number(form.concurrency);
  const maxRequests = Number(form.maxRequests);
  if (Number.isFinite(maxRps) && maxRps > 0) {
    args.push('--max-rps', String(maxRps));
  }
  if (Number.isFinite(concurrency) && concurrency > 0) {
    args.push('--concurrency', String(concurrency));
  }
  if (Number.isFinite(maxRequests) && maxRequests > 0) {
    args.push('--max-requests', String(maxRequests));
  }
  args.push('--timeout-ms', String(PRESET_BOUNTY.timeoutMs));

  const openapi = (form.openapi || '').trim();
  if (openapi) args.push('--openapi', openapi);

  const scopeMode = form.scopeMode || 'auto';
  if (scopeMode === 'auto') {
    args.push('--scope-file', writeAutoScope(target));
  } else if (scopeMode === 'manual') {
    const sf = (form.scopeFile || '').trim();
    if (!sf) throw new Error('Manual scope selected but scope file path is empty');
    args.push('--scope-file', sf);
  }

  const authEnv = (form.authEnv || '').trim();
  if (authEnv) args.push('--auth-env', authEnv);

  if (form.evidence === 'on') args.push('--evidence-pack');

  return args;
}

const server = http.createServer(async (req, res) => {
  const u = new URL(req.url || '/', `http://${req.headers.host}`);

  if (req.method === 'GET' && u.pathname === '/') {
    res.writeHead(200, { 'Content-Type': 'text/html; charset=utf-8' });
    res.end(HTML);
    return;
  }

  if (req.method === 'POST' && u.pathname === '/run') {
    let form;
    try {
      form = await parseForm(req);
    } catch {
      res.writeHead(400, { 'Content-Type': 'text/plain' });
      res.end('Bad request');
      return;
    }

    let cliArgs;
    try {
      cliArgs = buildArgs(form);
    } catch (e) {
      res.writeHead(400, { 'Content-Type': 'text/html; charset=utf-8' });
      res.end(
        `<!DOCTYPE html><html><head><meta charset="utf-8"/><title>Error</title></head><body><p>${escapeHtml(
          (/** @type {Error} */ (e)).message
        )}</p><p><a href="/">Back</a></p></body></html>`
      );
      return;
    }

    const child = spawn(process.execPath, [mainJs, ...cliArgs], {
      cwd: repoRoot,
      env: { ...process.env },
      windowsHide: true,
    });

    let out = '';
    const append = (b) => {
      out += b.toString('utf8');
      if (out.length > 2_000_000) out = out.slice(-1_500_000);
    };
    child.stdout?.on('data', append);
    child.stderr?.on('data', append);

    child.on('error', (err) => {
      append(`\n[spawn error] ${err.message}\n`);
    });

    child.on('close', (code) => {
      const cmdline = ['node', 'src/main.js', ...cliArgs.map((a) => (/\s/.test(a) ? JSON.stringify(a) : a))].join(' ');
      const page = `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="utf-8"/>
  <title>Run finished</title>
  <style>
    body { font-family: ui-monospace, monospace; background: #0f1419; color: #d0dce6; padding: 1rem; max-width: 960px; margin: 0 auto; }
    pre { white-space: pre-wrap; word-break: break-word; background: #15202b; padding: 1rem; border-radius: 8px; border: 1px solid #38444d; }
    a { color: #1d9bf0; }
    .cmd { font-size: 0.75rem; color: #8b98a5; margin-bottom: 1rem; }
    .exit { margin: 1rem 0; font-weight: 600; }
  </style>
</head>
<body>
  <p><a href="/">&larr; New run</a></p>
  <p class="exit">Exit code: ${code ?? 'unknown'}</p>
  <p class="cmd">Roughly: <code>${escapeHtml(cmdline)}</code></p>
  <pre>${escapeHtml(out || '(no output)')}</pre>
</body>
</html>`;
      res.writeHead(200, { 'Content-Type': 'text/html; charset=utf-8' });
      res.end(page);
    });
    return;
  }

  res.writeHead(404, { 'Content-Type': 'text/plain' });
  res.end('Not found');
});

server.listen(PORT, '127.0.0.1', () => {
  const url = `http://127.0.0.1:${PORT}/`;
  console.log(`Mythos launcher: ${url}`);
  console.log('Only reachable on this machine (127.0.0.1). Ctrl+C to stop.');
  const quiet = process.env.MYTHOS_LAUNCHER_NO_OPEN === '1';
  if (!quiet) openBrowser(url);
});
